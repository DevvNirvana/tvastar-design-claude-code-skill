#!/usr/bin/env python3
"""
/dark — Dark Mode Conversion Engine v1.0 (Phase 8.1)
Converts existing light designs to proper dark mode.
Not just adding dark: prefixes — understands dark mode color science.
Usage:
  python3 dark.py src/components/Hero.tsx
  python3 dark.py --audit src/       # check dark mode quality
  python3 dark.py --toggle           # generate DarkModeToggle component
"""
import sys, re, argparse
from pathlib import Path
from datetime import datetime
sys.path.insert(0, str(Path(__file__).parent))

# Dark mode color science rules
DARK_SCIENCE = {
    "bg":      {"light": ["#FFFFFF","#F9FAFB","#F3F4F6"], "dark": "#09090B",
                "rule": "Never pure black. zinc-950 (#09090B) has subtle warmth."},
    "surface": {"light": ["#FFFFFF","#F9FAFB"], "dark": "#18181B",
                "rule": "Cards/panels slightly lighter than bg. zinc-900 (#18181B)."},
    "border":  {"light": ["#E5E7EB","#D1D5DB"], "dark": "#27272A",
                "rule": "Borders very subtle in dark. zinc-800 (#27272A)."},
    "text":    {"light": ["#111827","#0F172A"],  "dark": "#FAFAFA",
                "rule": "Never pure white. zinc-50 (#FAFAFA) reduces eye strain."},
    "muted":   {"light": ["#6B7280","#64748B"],  "dark": "#71717A",
                "rule": "Muted text stays readable. zinc-500 (#71717A)."},
}

DARK_AUDIT_RULES = [
    {"id":"DK001","sev":"Critical","pattern":r'bg-black\b(?!\s*/)',
     "msg":"bg-black = pure #000000. Use bg-zinc-950 or bg-[#09090B] for premium dark."},
    {"id":"DK002","sev":"High",    "pattern":r'dark:bg-black\b',
     "msg":"dark:bg-black = pure black. Use dark:bg-zinc-950."},
    {"id":"DK003","sev":"High",    "pattern":r'text-white\b(?!.*dark:)',
     "msg":"text-white may lack dark mode variant. Check if dark:text-zinc-50 is needed."},
    {"id":"DK004","sev":"High",    "pattern":r'shadow-[a-z]+(?!.*dark:)',
     "msg":"Shadow visible in light mode may need dark mode glow variant."},
    {"id":"DK005","sev":"Medium",  "pattern":r'<img(?!.*dark:)',
     "msg":"Images in dark mode should have dark:brightness-90 to reduce harshness."},
    {"id":"DK006","sev":"Medium",  "pattern":r'border-gray-[2-3]00(?!.*dark:)',
     "msg":"Light border needs dark mode variant. Add dark:border-zinc-800."},
]

DARK_TOGGLE_TSX = '''\'use client\'
/**
 * DarkModeToggle — Generated by /dark --toggle {date}
 * Requires: next-themes (npm install next-themes)
 * Setup: wrap app in <ThemeProvider attribute="class"> in layout.tsx
 */
import {{ useTheme }} from 'next-themes'
import {{ useEffect, useState }} from 'react'
import {{ Sun, Moon }} from 'lucide-react'

export default function DarkModeToggle() {{
  const {{ theme, setTheme }} = useTheme()
  const [mounted, setMounted] = useState(false)
  useEffect(() => setMounted(true), [])
  if (!mounted) return <div className="h-9 w-9" aria-hidden="true" />

  const isDark = theme === 'dark'
  return (
    <button
      onClick={{() => setTheme(isDark ? 'light' : 'dark')}}
      className="flex h-9 w-9 cursor-pointer items-center justify-center rounded-[var(--radius-md)]
        border border-[var(--color-border)] bg-[var(--color-surface)] transition-all duration-150
        hover:border-[var(--color-primary)] hover:bg-[var(--color-primary)/8]
        focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[var(--color-primary)] focus-visible:ring-offset-2"
      aria-label={{isDark ? 'Switch to light mode' : 'Switch to dark mode'}}
      aria-pressed={{isDark}}
    >
      {{isDark
        ? <Sun  className="size-4" style={{{{ color: 'var(--color-text)' }}}} aria-hidden="true" />
        : <Moon className="size-4" style={{{{ color: 'var(--color-text)' }}}} aria-hidden="true" />
      }}
    </button>
  )
}}
'''

def audit_dark(code: str) -> list:
    issues = []
    for rule in DARK_AUDIT_RULES:
        if re.search(rule["pattern"], code, re.IGNORECASE):
            issues.append(rule)
    return issues

def convert_to_dark(code: str) -> tuple[str, int]:
    """Apply dark mode Tailwind class additions."""
    count = 0
    conversions = [
        (r'\bbg-white\b', 'bg-white dark:bg-zinc-950', 'bg-white'),
        (r'\bbg-gray-50\b', 'bg-gray-50 dark:bg-zinc-900', 'bg-gray-50'),
        (r'\bbg-gray-100\b', 'bg-gray-100 dark:bg-zinc-800', 'bg-gray-100'),
        (r'\btext-gray-900\b', 'text-gray-900 dark:text-zinc-50', 'text-gray-900'),
        (r'\btext-gray-700\b', 'text-gray-700 dark:text-zinc-300', 'text-gray-700'),
        (r'\btext-gray-500\b', 'text-gray-500 dark:text-zinc-400', 'text-gray-500'),
        (r'\bborder-gray-200\b', 'border-gray-200 dark:border-zinc-800', 'border-gray-200'),
        (r'\bborder-gray-300\b', 'border-gray-300 dark:border-zinc-700', 'border-gray-300'),
    ]
    for pattern, replacement, original in conversions:
        if re.search(pattern, code) and original not in replacement.split()[2:]:
            new_code = re.sub(pattern, replacement, code)
            if new_code != code:
                count += len(re.findall(pattern, code))
                code = new_code
    return code, count

def main():
    parser = argparse.ArgumentParser(description="/dark — Dark Mode Conversion Engine")
    parser.add_argument("file",      nargs="?", help="File to convert")
    parser.add_argument("--audit","-a", help="Audit directory for dark mode issues")
    parser.add_argument("--toggle",  action="store_true", help="Generate DarkModeToggle component")
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    print("\n╔══════════════════════════════════════════════════════╗")
    print("║        /dark — Dark Mode Engine                     ║")
    print("╚══════════════════════════════════════════════════════╝\n")

    if args.toggle:
        code = DARK_TOGGLE_TSX.format(date=datetime.now().strftime('%Y-%m-%d'))
        out = Path.cwd()/"src"/"components"/"DarkModeToggle.tsx"
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(code)
        print(f"  ✓ DarkModeToggle.tsx → {out}")
        print(f"  Install: npm install next-themes")
        print(f"  Setup:   Wrap app in <ThemeProvider attribute='class'> in layout.tsx\n")
        return

    if args.audit:
        root = Path(args.audit)
        total_issues = 0
        for ext in [".tsx",".ts",".jsx",".js"]:
            for f in root.rglob(f"*{ext}"):
                if "node_modules" in str(f): continue
                code = f.read_text(errors="ignore")
                issues = audit_dark(code)
                if issues:
                    print(f"  📄 {f.name} — {len(issues)} issue(s)")
                    for i in issues:
                        icon = "🔴" if i["sev"]=="Critical" else "🟠"
                        print(f"     {icon} [{i['id']}] {i['msg']}")
                    total_issues += len(issues)
        print(f"\n  Total dark mode issues: {total_issues}")
        if total_issues == 0: print("  ✅ Dark mode looks good!")
        print()
        return

    if args.file:
        path = Path(args.file)
        if not path.exists():
            print(f"  ⚠  File not found: {args.file}\n"); return
        code = path.read_text(errors="ignore")
        issues = audit_dark(code)
        new_code, count = convert_to_dark(code)
        print(f"  File: {args.file}")
        print(f"  Audit: {len(issues)} issues  |  Auto-conversions: {count}")
        for i in issues:
            icon = "🔴" if i["sev"]=="Critical" else "🟠" if i["sev"]=="High" else "🟡"
            print(f"  {icon} {i['msg']}")
        if not args.dry_run and count > 0:
            path.write_text(new_code)
            print(f"  ✓ Applied {count} dark mode class additions")
        elif args.dry_run:
            print(f"  [DRY RUN] Would apply {count} conversions")
        print()
        return

    # Default: show dark mode science
    print("  Dark Mode Color Science:\n")
    for key, data in DARK_SCIENCE.items():
        print(f"  {key.upper()}")
        print(f"    Light: {', '.join(data['light'][:2])}")
        print(f"    Dark:  {data['dark']}")
        print(f"    Rule:  {data['rule']}\n")
    print("  Commands:")
    print("  python3 dark.py src/Hero.tsx      Convert file to dark mode")
    print("  python3 dark.py --audit src/      Audit for dark mode issues")
    print("  python3 dark.py --toggle          Generate DarkModeToggle component\n")

if __name__ == "__main__":
    main()
