def main():
    parser = argparse.ArgumentParser(description="/apply — Approval-to-Production Code Generator")
    parser.add_argument("--component","-c", default="", help="Generate a specific component")
    parser.add_argument("--page","-p", default="landing", help="Page type: landing|dashboard|auth|pricing")
    parser.add_argument("--framework","-f", default="", help="Framework override")
    parser.add_argument("--out","-o", default="", help="Output directory")
    args = parser.parse_args()

    root = Path.cwd()

    print("\n╔══════════════════════════════════════════════════════╗")
    print("║        /apply — Production Code Generator           ║")
    print("╚══════════════════════════════════════════════════════╝\n")

    # Load approval
    approval = load_approved_tokens(root)
    if not approval:
        print("  ⛔ No approved design found.")
        print("  Run the workflow first:")
        print("  1. /design — generate your design system")
        print("  2. /preview — review the visual design")
        print("  3. /approved — stamp the approval")
        print("  4. /apply — generate production code\n")
        sys.exit(1)

    tokens = approval["tokens"]
    ts     = approval["timestamp"]
    print(f"  ✅ Using approved design from: {ts}")
    print(f"  Tokens: {tokens.get('primary','?')} · {tokens.get('font_heading','?')}\n")

    # Detect framework
    framework = args.framework or detect_framework(root)
    out_dir   = Path(args.out) if args.out else root / "src" / "components"
    out_dir.mkdir(parents=True, exist_ok=True)

    is_dark = tokens.get("bg","#fff") not in ("#FFFFFF","#ffffff","#fff","#F9FAFB","#FAFAFA")
    generated = []

    # Always output design tokens
    css_path = root / "app" / "globals.css" if (root/"app").exists() else root / "src" / "app" / "globals.css" if (root/"src"/"app").exists() else root / "globals.css"
    css_content = generate_globals_css(tokens, is_dark)
    css_path.parent.mkdir(parents=True, exist_ok=True)

    # Only write if no existing file to avoid overwriting
    if not css_path.exists():
        css_path.write_text(css_content)
        print(f"  ✓ globals.css → {css_path.relative_to(root) if css_path.is_relative_to(root) else css_path}")
    else:
        # Append token block
        print(f"  ℹ  globals.css already exists — appending token block to design-system/tokens.css")
        (root/"design-system"/"tokens.css").write_text(css_content)

    # Tailwind config
    tw_path = root / "tailwind.config.ts"
    if not tw_path.exists():
        tw_path.write_text(generate_tailwind_config(tokens, framework))
        print(f"  ✓ tailwind.config.ts")
    else:
        (root/"design-system"/"tailwind-config-additions.ts").write_text(generate_tailwind_config(tokens, framework))
        print(f"  ℹ  tailwind.config.ts exists → additions saved to design-system/tailwind-config-additions.ts")

    # Generate Hero component (always as starting point)
    comp_name, comp_code = generate_hero_component(tokens, framework)
    comp_path = out_dir / comp_name
    comp_path.write_text(comp_code)
    generated.append(str(comp_path))
    print(f"  ✓ {comp_name} → {comp_path.relative_to(root) if comp_path.is_relative_to(root) else comp_path}")

    # Auto-run review
    print(f"\n  Running /review on generated files...")
    review_result = review_code(comp_code, comp_name, ["react","tailwind","nextjs" if "next" in framework else "react"])
    score = review_result["overall"]
    g, icon = grade(score)
    crits = review_result["counts"]["critical"]
    highs = review_result["counts"]["high"]

    print(f"  {icon} Review score: {score}/100 (Grade {g})")
    print(f"     Critical: {crits}  High: {highs}  Medium: {review_result['counts']['medium']}")

    if crits > 0:
        print(f"\n  ⛔ {crits} Critical issues detected — run /review for details")
    elif highs > 0:
        print(f"\n  ⚠️  {highs} High issues — run /review [file] for fix instructions")
    else:
        print(f"\n  ✅ All clear — production-ready!")

    print(f"\n  Generated files:")
    for f in generated:
        print(f"  • {f}")

    print(f"\n  Install commands (run in project root):")
    print(f"  npm install framer-motion")
    print(f"  npx shadcn@latest add \"https://reactbits.dev/r/Aurora\"")
    print(f"  npx shadcn@latest add \"https://reactbits.dev/r/SplitText\"")
    print(f"  npx shadcn@latest add \"https://reactbits.dev/r/BlurText\"")
    print(f"  npx magicui-cli@latest add shimmer-button\n")

if __name__ == "__main__":
    main()
