def load_approved_tokens(root: Path) -> dict | None:
    approved_dir = root / "design-system" / "approved"
    if not approved_dir.exists():
        return None
    snaps = sorted(approved_dir.glob("*.json"), reverse=True)
    if not snaps:
        return None
    data = json.loads(snaps[0].read_text())
    if data.get("status") == "approved":
        return data
    return None

def detect_framework(root: Path) -> str:
    try:
        result = subprocess.run(
            ["python3", str(Path(__file__).parent / "detect_stack.py")],
            capture_output=True, text=True, cwd=root
        )
        for line in result.stdout.split("\n"):
            if line.startswith("STACK_JSON:"):
                stack = json.loads(line[11:])
                return stack.get("framework", "react")
    except Exception:
        pass
    return "react"

